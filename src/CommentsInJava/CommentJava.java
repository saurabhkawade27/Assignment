package CommentsInJava;

public class CommentJava {

	public static void main(String[] args) {

		// 1. Single-line comments. ==> // --> Cntrl+/ (comment/uncomment)
		// 2. Multi-line comments. ==> /*...*/ --> Cntrl+Shift+/ (comment) ,
		// Cntrl+Shift+\ (uncomment)

		/*
		 * This line refers to the main method. This is always going to act like a
		 * starting point for Java program execution. If there is no main method, we
		 * cannot execute any program.
		 */

		// This is a print statement. It can print the information and move to the next
		// line.
		System.out.println("Hello, World!");

	}

}
